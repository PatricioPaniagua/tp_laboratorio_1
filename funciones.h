#ifndef FUNCIONES_H_INCLUDED
#define FUNCIONES_H_INCLUDED

int sumar(int a, int b)
{
    int suma;
    suma = a+b;
    return suma;
}
int restar(int a, int b)
{
    int resta;
    resta = a-b;
    return resta;
}
int dividir(int a, int b)
{
    int división;
    división = a/b;
    return división;
}
int multiplicar(int a, int b)
{
    int multiplicación;
    multiplicación = a*b;
    return multiplicación;
}
#endif // FUNCIONES_H_INCLUDED
